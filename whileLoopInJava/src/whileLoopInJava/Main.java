package whileLoopInJava;

public class Main {

	public static void main(String[] args) {
		//While Loop in Java
		
		int i = 1;
		
		while(i<10) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("While loop is over!");
		
		int a =1;
		while(a<10) {
			System.out.println(a);
			a+=2;
		}
		System.out.println("While loop is over for odd numbers");

		int b = 0 ;
		while (b<10) {
			System.out.println(b);
			b+=2;
		}
		System.out.println("b while loop is over.");
	}

}
